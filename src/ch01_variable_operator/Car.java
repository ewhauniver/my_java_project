package ch01_variable_operator;

public class Car {
    public static void main(String[] args) {
        int number ;
        String brand ;
        String model ;
        double efficiency ;

        number = 1 ;
        brand = "BMW" ;
        model = "X6" ;
        efficiency = 7.8 ;

        System.out.println("번호 : " + number) ;
        System.out.println("브랜드 : " + brand) ;
        System.out.println("모델명 : " + model) ;
        System.out.println("연비 : " + efficiency) ;

    }
}
